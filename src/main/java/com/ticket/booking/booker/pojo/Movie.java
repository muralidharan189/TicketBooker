package com.ticket.booking.booker.pojo;

public class Movie {
	private String movieNumber;
	private String movieName;
	private String movieTiming;
	public String getMovieNumber() {
		return movieNumber;
	}
	public void setMovieNumber(String movieNumber) {
		this.movieNumber = movieNumber;
	}
	public String getMovieName() {
		return movieName;
	}
	public void setMovieName(String movieName) {
		this.movieName = movieName;
	}
	public String getMovieTiming() {
		return movieTiming;
	}
	public void setMovieTiming(String movieTiming) {
		this.movieTiming = movieTiming;
	}
}
